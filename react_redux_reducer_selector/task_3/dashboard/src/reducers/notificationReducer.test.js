import { notifReducer } from "./notificationReducer";
import {
  markAsAread,
  setNotificationFilter,
  fetchNotifications
} from "../actions/notificationActionCreators";

describe('tests for botifReducer', () => {
  const dataTest = [
    {
      id: 1,
      type: "default",
      value: "New course available"
    },
    {
      id: 2,
      type: "urgent",
      value: "New resume available"
    },
    {
      id: 3,
      type: "urgent",
      value: "New data available"
    }
  ];
  it('should send the good data with FETCH action', () => {
    const previousState = {
      notifications: [],
      filter: 'DEFAULT',
    };
    const reducerTest = notifReducer(previousState, fetchNotifications(dataTest));
    expect(reducerTest).toEqual({
      filter: "DEFAULT",
      notifications: [
        {
          id: 1,
          isRead: false,
          type: "default",
          value: "New course available"
        },
        {
          id: 2,
          isRead: false,
          type: "urgent",
          value: "New resume available"
        },
        {
          id: 3,
          isRead: false,
          type: "urgent",
          value: "New data available"
        }
      ]
    })
  });

  it('should send the good data with MARK_AS_READ action', () => {
    const previousState = {
      filter: "DEFAULT",
      notifications: [
        {
          id: 1,
          isRead: false,
          type: "default",
          value: "New course available"
        },
        {
          id: 2,
          isRead: false,
          type: "urgent",
          value: "New resume available"
        },
        {
          id: 3,
          isRead: false,
          type: "urgent",
          value: "New data available"
        }
      ]
    };
    const reducerTest = notifReducer(previousState, markAsAread(2));
    expect(reducerTest).toEqual({
      filter: "DEFAULT",
  notifications: [
        {
          id: 1,
          isRead: false,
          type: "default",
          value: "New course available"
        },
        {
          id: 2,
          isRead: true,
          type: "urgent",
          value: "New resume available"
        },
        {
          id: 3,
          isRead: false,
          type: "urgent",
          value: "New data available"
        }
      ]
    })
  });

  it('should change the type with SET_TYPE_FILTER action', () => {
    const previousState = {
      filter: "DEFAULT",
      notifications: [
        {
          id: 1,
          isRead: false,
          type: "default",
          value: "New course available"
        },
        {
          id: 2,
          isRead: false,
          type: "urgent",
          value: "New resume available"
        },
        {
          id: 3,
          isRead: false,
          type: "urgent",
          value: "New data available"
        }
      ]
    };
    const testReducer = notifReducer(previousState, setNotificationFilter('URGENT'));
    expect(testReducer).toEqual({
      filter: "URGENT",
      notifications: [
        {
          id: 1,
          isRead: false,
          type: "default",
          value: "New course available"
        },
        {
          id: 2,
          isRead: false,
          type: "urgent",
          value: "New resume available"
        },
        {
          id: 3,
          isRead: false,
          type: "urgent",
          value: "New data available"
        }
      ]
    })
  });
})
